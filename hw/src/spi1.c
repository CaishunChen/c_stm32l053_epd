/*******************************************************************************
 *  function :    spi1_readByte
 ******************************************************************************/
uint8_t
spi1_readByte(pfSpiCSHandler pfCSHandler) {

	uint8_t u8Data = 0x00;

	pfCSHandler(GPIO_STATE_CLEAR);


	pfCSHandler(GPIO_STATE_SET);

	return (u8Data);
}


